                    <h2>Государственное автономное учреждение Московской области &laquo;Московская областная
                        государственная экспертиза&raquo; (ГАУ МО&nbsp;&laquo;Мособлгосэкспертиза&raquo;) создано в&nbsp;соответствии
                        с&nbsp;Постановлением Правительства Московской области от&nbsp;22.06.2001
                        &#8470;&nbsp;188/21.<br>Учредитель&nbsp;&mdash; Московская область.<br>Учреждение
                        подведомственно Министерству экономики и&nbsp;финансов Московской области.</h2>
                    <div class="article__text article__text--cols">

                        <p>За&nbsp;прошедшее с&nbsp;момента образования годы ГАУ МО&nbsp;&laquo;Мособлгосэкспертиза&raquo;
                            стало одним из&nbsp;самых успешных и&nbsp;динамично развивающихся предприятий строительного
                            комплекса Московской области. В&nbsp;настоящее время в&nbsp;учреждении создана структура для
                            выполнения единой государственной экспертизы проектной документации. В&nbsp;государственном
                            учреждении работают 92&nbsp;специалистов-экспертов, занятых проведением государственной
                            экспертизы проектной документации и&nbsp;результатов инженерных изысканий.</p>
                        <p>Ежегодно в&nbsp;ГАУ МО&nbsp;&laquo;Мособлгосэкспертиза&raquo; проходят экспертизу около
                            полутора тысяч различных проектов на&nbsp;объекты строительства, возводимые на&nbsp;территории
                            Московской области. С&nbsp;2001 года рассмотрено более 100&nbsp;тыс. проектов.</p>
                        <p>По&nbsp;заданиям Правительства Московской области, министерств и&nbsp;ведомств Московской
                            области учреждением выполняется значительный объем работ по&nbsp;проверке сметной
                            документации по&nbsp;объектам, финансируемым из&nbsp;областного бюджета и&nbsp;бюджетов
                            муниципальных образований, на&nbsp;предмет обоснованности и&nbsp;достоверности использования
                            средств, соответствия нормативам в&nbsp;области сметного нормирования и&nbsp;ценообразования.</p>
                        <p>Значителен вклад, который ГАУ МО&nbsp;&laquo;Мособлгосэкспертиза&raquo; вносит в&nbsp;экономику
                            Московской области. В&nbsp;результате корректировки проектной документации по&nbsp;замечаниям
                            и&nbsp;предложениям государственной экспертизы экономия бюджетных средств составляет
                            несколько миллиардов рублей в&nbsp;год. Экономия средств бюджета Московской области с&nbsp;2001
                            года благодаря деятельности учреждения составила более 500 млн рублей.</p>
                        <p>ГАУ Московской области &laquo;Мособлгосэкспертиза&raquo; является активным участником
                            Ассоциации экспертиз России (АЭР), директор учреждения Игорь Горячев&nbsp;&mdash; член
                            правления АЭР.</p>

                        <h4>Основные направления деятельности:</h4>
                        <ul>
                            <li>проведение государственной экспертизы проектной, в&nbsp;том числе сметной документации и&nbsp;результатов
                                инженерных изысканий, выполненных для строительства, реконструкции, капитального ремонта
                                объектов, расположенных на&nbsp;территории Московской области;
                            </li>
                            <li>содействие в&nbsp;проведении государственной политики в&nbsp;сфере ценообразования в&nbsp;строительстве
                                на&nbsp;территории Московской области;
                            </li>
                            <li>проведение проверки сметной стоимости объектов строительства, реконструкции,
                                капитального ремонта в&nbsp;Московской области на&nbsp;предмет эффективности, экономии,
                                обоснованности и&nbsp;достоверности использования бюджетных средств;
                            </li>
                            <li>развитие цифрового информационного моделирования (ЦИМ) в&nbsp;проектировании и&nbsp;строительстве;</li>
                            <li>проведение негосударственной экспертизы проектной документации и&nbsp;результатов
                                инженерных изысканий;
                            </li>
                            <li>технологический и&nbsp;ценовой аудит публичных инвестиционных проектов;</li>
